  /// Get categories with counts
  static Map<String, int> getCategoryCounts() {
    final Map<String, int> counts = {};
    for (var product in products) {
      final category = product['category'] as String;
      counts[category] = (counts[category] ?? 0) + 1;
    }
    return counts;
  }

  /// Get products by category
  static List<Map<String, dynamic>> getByCategory(String category) {
    return products.where((p) => p['category'] == category).toList();
  }

  /// Search products
  static List<Map<String, dynamic>> search(String query) {
    final lowerQuery = query.toLowerCase();
    return products.where((p) {
      final name = (p['name'] as String).toLowerCase();
      final brand = (p['brand'] as String).toLowerCase();
      final category = (p['category'] as String).toLowerCase();
      return name.contains(lowerQuery) ||
          brand.contains(lowerQuery) ||
          category.contains(lowerQuery);
    }).toList();
  }

  /// Get all categories
  static List<String> get categories {
    return products.map((p) => p['category'] as String).toSet().toList()..sort();
  }
